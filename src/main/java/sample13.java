//インクリメント演算子とデクリメント演算子
public class sample13 {
    public static void main(String[] args) {
        int num = 5;


        num++;
        System.out.println(num);

        num--;
        System.out.println(num);

        ++num;
        System.out.println(num);

        --num;
        System.out.println(num);

    }
}
