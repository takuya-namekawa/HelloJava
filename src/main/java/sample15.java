//関係演算子と等価演算子
public class sample15 {
    public static void main(String[] args) {
        int a = 7;
        int b = 4;


        System.out.println("a = " + a);
        System.out.println("b = " + b);


        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
