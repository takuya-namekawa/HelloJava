//条件演算子(三項演算子)
public class sample17 {
    public static void main(String[] args) {
        int score;


        score = 70;
        System.out.println("Score=" + score + ":");
        System.out.println(score > 70 ? "ok" : "NG");

        score = 84;
        System.out.println("Score=" + score + ":");
        System.out.println(score > 70 ? "OK" : "NG");
    }
}
